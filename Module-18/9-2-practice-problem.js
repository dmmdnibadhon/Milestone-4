var marks = prompt("Enter your marks: ");


if(marks >= 80){
    console.log("A grade");
}
else if(marks >= 60){
    console.log("B grade");
}
else if(marks >= 50){
    console.log("C grade");
}
else if(marks >= 40){
    console.log("D grade");
}
else if(marks >= 33){
    console.log("E grade");
}
else
console.log("F grade Fail");