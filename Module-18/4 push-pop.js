// add an array in the last by push
var x = [1, 2, 3, 4];
x.push(5);
console.log(x);

// add an array in the first by unshift
var y = ["a", "b", "c", "d"];
y.unshift("e");
console.log(y);

// remove an array by pop
var a = [1, 2, 3];
a.pop()
console.log(a);

// add an array in the first by shift
var y = ["a", "b", "c", "d"];
y.shift();
console.log(y);